addEventHandler('onPlayerLogin',root,function()
    local acc = getPlayerAccount(source)
    local storedeggs = getAccountData(acc,'eggs')
    
    if storedeggs == false then
        setElementData( source, "eggs", 0 )
    else
        setElementData( source, "eggs", storedeggs )
    end
end)


